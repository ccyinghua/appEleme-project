<!-- 购买控件：商品数量加减图标 -->
<template>
  <div class="cartcontrol">
    <transition name="move">
      <div class="cart-decrease" v-show="food.count>0" @click.stop.prevent="decreaseCart">
        <span class="inner icon-remove_circle_outline"></span>
      </div>
    </transition>
    <div class="cart-count" v-show="food.count>0">{{food.count}}</div>
    <div class="cart-add icon-add_circle" @click.stop.prevent="addCart"></div>
  </div>
</template>

<script type="text/ecmascript-6">
  import Vue from 'vue'
  export default {
    props: {
      food: {
        type: Object
      }
    },
    methods: {
      addCart(event) {    // 加
        if (!event._constructed) {  // 防止在PC端点击两次，详情见goods.vue的better-scroll
          return
        }
        console.log('click')
        if (!this.food.count) {
          // this.food.count = 1   // 我们添加一个food不存在的属性的时候，这样直接赋值是不可以的，检测不到属性的变化。要用的Vue的接口
          Vue.set(this.food, 'count', 1)  // 通过Vue.set添加属性，变化就能被观测到，就能通知到DOM发生变化
        } else {
          this.food.count ++
        }
        this.$root.eventHub.$emit('add', event.target)   // 派发事件
      },
      decreaseCart(event) {    // 减
        if (!event._constructed) {  // 防止在PC端点击两次，详情见goods.vue的better-scroll
          return
        }
        if (this.food.count) {
          this.food.count --
        }
      }
    }
  }
</script>

